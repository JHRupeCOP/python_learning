# Math with variables 

# add numbers
number_1 = 5
number_2 = 10
print(number_1 + number_2)

# concatenate strings
fruit_1 = "apples"
fruit_2 = "oranges"
print(fruit_1 + fruit_2)
print("I like " + fruit_1 + " and I like " + fruit_2)

# using a f-string
print(f"I like {fruit_1} and I like {fruit_2}")

print(fruit_1 * 5)
